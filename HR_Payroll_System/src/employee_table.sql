create table "PRS".EMPLOYEE
(
	USERNAME VARCHAR(255) not null primary key,
	FIRST_NAME VARCHAR(255),
	LAST_NAME VARCHAR(255),
	IC_PASSPORT_NO INTEGER,
	EMAIL VARCHAR(255),
	AGE INTEGER,
	PASSWORD VARCHAR(255),
	ALLOWANCE DOUBLE,
	BASIC_SALARY DOUBLE,
	POSITION VARCHAR(255),
	DEDUCTION DOUBLE,
	EMPLOYEE_ID VARCHAR(255)
)